mark=int(input("Enter the Mark :"))

if mark <25:
    print("F Grade")
elif mark >=25 and mark<45:
    print("E Grade")
elif mark >=45 and mark<50:
    print("D Grade")
elif mark >=50 and mark<60:
    print("C Grade")
elif mark >=60 and mark<80:
    print("B Grade")
elif mark>=80:
    print("A Grade")